export interface IIcon {
  /**
   * Icon filename
   * @type {string}
   * @memberof IIcon
   */
  filename: string;
  /**
   * If set to true, the icon is marked as last
   * @type {boolean}
   * @memberof IIcon
   */
  last: boolean;
  /**
   * Icon's name
   * @type {string}
   * @memberof IIcon
   */
  name: string;
}